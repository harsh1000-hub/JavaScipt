// boolean and comparison operator
console.log(2<=2);
// == -> in this equality only check the value
// === -> in this equality first check the datatype than value
let num1=2;
let num2="2";
console.log(num1==num2); // only check the value
console.log(num1 === num2); // check first datatype than value

// != -> only check the value
// !== -> check the datatype first than the value
let firstNum = 2;
let secondNum = "2";
console.log(firstNum != secondNum);
console.log(firstNum !== secondNum);